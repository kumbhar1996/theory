
# different types of method

# 1. Append-->add one element at the end of list

l1=[10,20,'akshay',40]
l1.append('Raveena')
print(l1)

# 2. pop ---> remove last element from list

l1.pop()
print(l1)

# 3. insert -->insert the element at required indexing position

l1.insert(0,'Sagar')
print(l1)

# 4. remove --> remove the particular element from list

l1.remove(10)
print(l1)

# 5. sort --> used for sorting alphabetically 

l1=[1,58,63,128,100,25]
l1.sort()
print(l1)